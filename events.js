function audio_to_millis(title) {
	var result
	if (!title) {
		result = null
	}
	match = title.match(/^a\d+_(t-)?(\d\d\d)(\d\d)(\d\d)(-hold\d\d|-cm)?/)
	if (match) {	
		hour   = match[2]
		minute = match[3]
		second = match[4]
		result = hour * HOUR + minute * MINUTE + second * SECOND
		if (match[1]) {
			result *= -1
		}
	} else {
		result = NaN
	}
	// console.log(title, result, match[5])
	return result
}

